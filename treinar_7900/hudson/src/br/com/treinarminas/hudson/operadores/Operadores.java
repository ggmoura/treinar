package br.com.treinarminas.hudson.operadores;

public class Operadores {
	public static void main(String[] args) {
		int valorUm   = 10;
		int valorDois = 20;
		int soma = valorUm + valorDois;
		System.out.println( "A soma de valorUm + valorDois e: " + soma );
		float divisao = valorUm / (float)valorDois;
		System.out.println( "A divisao de valorUm + valorDois e: " + divisao );
	}
}
